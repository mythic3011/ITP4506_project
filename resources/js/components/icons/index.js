export function getIcon(name, size = null) {
    const icon = icons[name];
    if (!icon) return '';

    // Check if size is provided and is a valid number
    if (size !== null && (typeof size !== 'number' || size <= 0)) {
        console.error('Invalid size provided for icon:', name);
        return icon; // Return the original icon without size adjustment
    }

    return icon.replace(/width="\d+"/g, `width="${size || '20'}"`).replace(/height="\d+"/g, `height="${size || '20'}"`); // Default to '20'
}